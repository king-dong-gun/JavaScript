// 일치 연산자
let num = 10;
let str = "10";
if(num == str) console.log("같다");  // 값만 비교한다.
if(num === str) console.log("같다");  // 값 + 타입을 비교한다.
else console.log("다르다");

console.log(0 == false);            // 동등 연산자
console.log(0 === false);           // 일치 연산자
console.log(" " == false);
console.log(" " === false);
console.log(null == undefined);
console.log(null === undefined);


